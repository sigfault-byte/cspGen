#ifndef BYTE_SIGNATURE_H
#define BYTE_SIGNATURE_H

// Punctuation and symbols
#define LT          0x3C  // '<'
#define GT          0x3E  // '>'
#define SLASH       0x2F  // '/'
#define SPACE       0x20  // ' '
#define TAB         0x09  // '\t'
#define NEWLINE     0x0A  // '\n'
#define CR          0x0D  // '\r'
#define QUOTE       0x22  // '"'
#define APOSTROPHE  0x27 // '\''
#define EQUAL       0x3D  // '='
#define PARENTHESIS 0x28 // '('

// Letters for tag/attr parsing
#define S  0x73  // 's'
#define C  0x63  // 'c'
#define R  0x72  // 'r'
#define I  0x69  // 'i'
#define P  0x70  // 'p'
#define T  0x74  // 't'

#define H  0x68  // 'h'
#define M  0x6D  // 'm'
#define L  0x6C  // 'l'

#define E  0x65  // 'e'
#define A  0x61  // 'a'
#define D  0x64  // 'd'

#define B  0x62  // 'b'
#define O  0x6F  // 'o'
#define Y  0x79  // 'y'

#define N  0x6E  // `n`

#endif